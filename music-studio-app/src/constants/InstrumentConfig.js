// Get all instrument IDs
export const INSTRUMENT_IDS = Object.keys(INSTRUMENTS);

// Get instruments by category
export const getInstrumentsByCategory = (category) => {
    return Object.values(INSTRUMENTS).filter(inst => inst.category === category);
};

// Get instrument config by ID
export const getInstrumentConfig = (id) => {
    return INSTRUMENTS[id] || null;
};

// Check if instrument has specific feature
export const instrumentHasFeature = (id, feature) => {
    const config = getInstrumentConfig(id);
    return config ? config[feature] === true : false;
};
